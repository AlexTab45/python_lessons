text=input("Введите любой текст: ")
a=text.lower().count("a")
print(f"a = {a}")
e=text.lower().count("e")
print(f"e = {e}")
i=text.lower().count("i")
print(f"i = {i}")
o=text.lower().count("o")
print(f"o = {o}")
u=text.lower().count("u")
print(f"u = {u}")
y=text.lower().count("y")
print(f"y = {y}")
print(f"Общее количество гласных букв = {a+e+i+o+u+y}")